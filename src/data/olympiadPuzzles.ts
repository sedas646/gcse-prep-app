  const dateNum = parseInt(dateStr.split('-').join(''), 10);
  const mathsIndex = dateNum % mathsOlympiad.length;
  const physicsIndex = (dateNum + 7) % physicsOlympiad.length; // offset to avoid same pattern
  return {
    maths: mathsOlympiad[mathsIndex],
    physics: physicsOlympiad[physicsIndex],
  };
